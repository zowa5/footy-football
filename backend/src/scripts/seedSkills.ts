const seedSkills = async () => {
  try {
    // Connect to MongoDB
    await mongoose.connect(
      process.env.MONGODB_URI ||
        "mongodb://localhost:27017/pes-football-management"
    );

    console.log("üå± Seeding skill templates...");

    // Clear existing skill templates
    await SkillTemplate.deleteMany({});

    // Insert skill templates
    await SkillTemplate.insertMany(skillTemplates);

    console.log(
      `‚úÖ Successfully seeded ${skillTemplates.length} skill templates`
    );

    // Disconnect from MongoDB
    await mongoose.disconnect();
    console.log("üì° Disconnected from MongoDB");
  } catch (error) {
    console.error("‚ùå Error seeding skill templates:", error);
    process.exit(1);
  }
};

// Run the seeder
seedSkills();
